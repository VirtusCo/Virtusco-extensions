  private addPythonImport(
    imports: Map<string, Set<string>>,
    fullType: string,
    category: 'msg' | 'srv' | 'action' = 'msg'
  ): void {
    const parts = fullType.split('/');
    if (parts.length !== 2) return;
    const importPath = `${parts[0]}.${category}`;
    if (!imports.has(importPath)) {
      imports.set(importPath, new Set());
    }
    imports.get(importPath)!.add(parts[1]);
  }

  private needsSensorQoS(node: CanvasNodeConfig): boolean {
    const sensorTopics = ['/scan', '/image', '/imu', '/camera', '/pointcloud', '/range'];
    for (const pub of node.publishers) {
      if (sensorTopics.some((t) => pub.topic.includes(t))) return true;
    }
    for (const sub of node.subscribers) {
      if (sensorTopics.some((t) => sub.topic.includes(t))) return true;
    }
    return false;
  }

  private inferPythonQoS(topic: string, msgType: string): string {
    if (this.isSensorTopic(topic, msgType)) return 'SENSOR_QOS';
    return '10';
  }

  private inferCppQoS(topic: string, msgType: string): string {
    if (this.isSensorTopic(topic, msgType)) {
      return 'rclcpp::SensorDataQoS().reliable()';
    }
    if (topic === '/tf_static') {
      return 'rclcpp::QoS(10).transient_local()';
    }
    return '10';
  }

  private isSensorTopic(topic: string, msgType: string): boolean {
    const sensorTopics = ['/scan', '/image_raw', '/camera', '/imu', '/pointcloud', '/range'];
    const sensorMsgs = ['sensor_msgs/LaserScan', 'sensor_msgs/Image', 'sensor_msgs/Imu',
      'sensor_msgs/PointCloud2', 'sensor_msgs/Range', 'sensor_msgs/CameraInfo'];
    return sensorTopics.some((t) => topic.includes(t)) || sensorMsgs.includes(msgType);
  }

  private topicToVarName(topic: string, prefix: string): string {
    return prefix + '_' + topic.replace(/^\//, '').replace(/\//g, '_').replace(/[^a-zA-Z0-9_]/g, '');
  }

  private topicToCallbackName(topic: string): string {
    return topic.replace(/^\//, '').replace(/\//g, '_').replace(/[^a-zA-Z0-9_]/g, '');
  }

  private pythonDefaultValue(type: string, value: string): string {
    if (type === 'bool') return value === 'true' || value === 'True' ? 'True' : 'False';
    if (type === 'int') return value || '0';
    if (type === 'float') return value || '0.0';
    return `'${value || ''}'`;
  }

  private cppDefaultValue(type: string, value: string): string {
    if (type === 'bool') return value === 'true' ? 'true' : 'false';
    if (type === 'int') return value || '0';
    if (type === 'float') return value || '0.0';
    return `"${value || ''}"`;
  }

  private paramTypeToCpp(type: string): string {
    if (type === 'bool') return 'bool';
    if (type === 'int') return 'int64_t';
    if (type === 'float') return 'double';
    return 'std::string';
  }

  private yamlValue(type: string, value: string): string {
    if (type === 'bool') return value === 'true' || value === 'True' ? 'true' : 'false';
    if (type === 'int') return value || '0';
    if (type === 'float') return value || '0.0';
    return `"${value || ''}"`;
  }
