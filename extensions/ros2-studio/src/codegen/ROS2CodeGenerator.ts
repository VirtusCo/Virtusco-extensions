/**
 * Extracts the package name from a full message type.
 * "sensor_msgs/LaserScan" -> "sensor_msgs"
 */
export function msgTypeToPackage(msgType: string): string {
  return msgType.split('/')[0] ?? '';
}

/**
 * Generates the C++ include for a message type.
 * "sensor_msgs/LaserScan" -> '#include <sensor_msgs/msg/laser_scan.hpp>'
 */
export function msgTypeToInclude(msgType: string, language: 'cpp' | 'python'): string {
  const parts = msgType.split('/');
  if (parts.length !== 2) return '';
  const pkg = parts[0];
  const name = parts[1];

  if (language === 'cpp') {
    const snakeName = camelToSnake(name);
    return `#include <${pkg}/msg/${snakeName}.hpp>`;
  }
  return `from ${pkg}.msg import ${name}`;
}

/**
 * Generates the C++ include for a service type.
 */
export function msgTypeToSrvInclude(srvType: string, language: 'cpp' | 'python'): string {
  const parts = srvType.split('/');
  if (parts.length !== 2) return '';
  const pkg = parts[0];
  const name = parts[1];

  if (language === 'cpp') {
    const snakeName = camelToSnake(name);
    return `#include <${pkg}/srv/${snakeName}.hpp>`;
  }
  return `from ${pkg}.srv import ${name}`;
}

/**
 * Generates the C++ include for an action type.
 */
export function msgTypeToActionInclude(actionType: string, language: 'cpp' | 'python'): string {
  const parts = actionType.split('/');
  if (parts.length !== 2) return '';
  const pkg = parts[0];
  const name = parts[1];

  if (language === 'cpp') {
    const snakeName = camelToSnake(name);
    return `#include <${pkg}/action/${snakeName}.hpp>`;
  }
  return `from ${pkg}.action import ${name}`;
}

/**
 * Converts a message type to its C++ namespaced form.
 * "sensor_msgs/LaserScan" -> "sensor_msgs::msg::LaserScan"
 */
export function msgTypeToCppType(msgType: string): string {
  const parts = msgType.split('/');
  if (parts.length !== 2) return msgType;
  return `${parts[0]}::msg::${parts[1]}`;
}

/**
 * Extracts just the class name from a full message type.
 * "sensor_msgs/LaserScan" -> "LaserScan"
 */
export function msgTypeToClassName(msgType: string): string {
  const parts = msgType.split('/');
  return parts[parts.length - 1];
}

/**
 * Infers QoS profile based on topic name and message type.
 */
export function inferQoS(topic: string, msgType: string): string {
  const sensorTopics = ['/scan', '/image_raw', '/camera', '/imu', '/pointcloud', '/range'];
  const sensorMsgs = ['sensor_msgs/LaserScan', 'sensor_msgs/Image', 'sensor_msgs/Imu',
    'sensor_msgs/PointCloud2', 'sensor_msgs/Range'];

  if (sensorTopics.some((t) => topic.includes(t)) || sensorMsgs.includes(msgType)) {
    return 'SensorDataQoS';
  }
  if (topic === '/tf_static') {
    return 'StaticBroadcasterQoS';
  }
  return 'default';
}

// ── String Utilities ────────────────────────────────────────────────

function toSnakeCase(name: string): string {
  return name
    .replace(/([A-Z])/g, '_$1')
    .replace(/[\s-]+/g, '_')
    .replace(/^_/, '')
    .replace(/__+/g, '_')
    .toLowerCase();
}

function toPascalCase(name: string): string {
  return name
    .replace(/[_\s-]+(.)/g, (_, c) => c.toUpperCase())
    .replace(/^(.)/, (_, c) => c.toUpperCase())
    .replace(/[^a-zA-Z0-9]/g, '');
}

function camelToSnake(name: string): string {
  return name
    .replace(/([A-Z])/g, '_$1')
    .replace(/^_/, '')
    .toLowerCase();
}
